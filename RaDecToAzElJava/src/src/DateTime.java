package src;

public class DateTime {
	int year;
	int mon;
	int day;
	int hour;
	int min;
	double sec;
	
	public DateTime(int year, int mon, int day, int hour, int min, double sec){
		
		this.year = year;
		this.mon = mon;
		this.day = day;
		this.hour =  hour ;
		this.min = min;
		this.sec = sec;
	}
}